#include "main.h"

/**
 * print_sign - prints sign of digits
 *@n: assigned to serve as a checker
 * Return: if statements
 */

int print_sign(int n)
{
	int value;

	if (n > 0)
	{
		value = 1;
		_putchar('+');
	}
	else if (n == 0)
	{
		value = 0;
		_putchar('0');
	}
	else
	{
		value = -1;
		_putchar('-');
	}

	return (value);
}
